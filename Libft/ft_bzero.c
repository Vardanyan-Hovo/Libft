#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*d;

	if (n != 0)
	{
		d = s;
		i = 0;
		while (i < n)
		{
			d[i] = '\0';
			i++;
		}
	}
}
